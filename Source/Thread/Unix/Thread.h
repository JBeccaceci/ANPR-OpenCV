#ifndef _THREAD_H
#define _THREAD_H

////////////////////////////////////////////////////////////
/// Includes
////////////////////////////////////////////////////////////
#include <thread>

////////////////////////////////////////////////////////////
/// The thread funcion
////////////////////////////////////////////////////////////
typedef void(*ThreadRoutine)(void* threadData);

////////////////////////////////////////////////////////////
/// Thread data struct
////////////////////////////////////////////////////////////
struct threadData 
{
	ThreadRoutine Routine;
	void *        Data;
};

////////////////////////////////////////////////////////////
/// Create a new thread
////////////////////////////////////////////////////////////
bool Thread_Create(pthread_t &ThreadID, ThreadRoutine Routine, void * ThreadData);

////////////////////////////////////////////////////////////
/// Wait the thread
////////////////////////////////////////////////////////////
void Thread_Wait(pthread_t ThreadID);

////////////////////////////////////////////////////////////
/// Terminate thread by ID
////////////////////////////////////////////////////////////
void Thread_Terminate(pthread_t ThreadID);



#endif
